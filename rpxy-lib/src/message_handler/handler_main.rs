#[derive(Clone, Builder)]
/// HTTP message handler for requests from clients and responses from backend applications,
/// responsible to manipulate and forward messages to upstream backends and downstream clients.
pub struct HttpMessageHandler<C>
where
  C: Send + Sync + Connect + Clone + 'static,
{
  forwarder: Arc<Forwarder<C>>,
  pub(super) globals: Arc<Globals>,
  app_manager: Arc<BackendAppManager>,
}

impl<C> HttpMessageHandler<C>
where
  C: Send + Sync + Connect + Clone + 'static,
{
  /// Handle incoming request message from a client.
  /// Responsible to passthrough responses from backend applications or generate synthetic error responses.
  pub async fn handle_request(
    &self,
    req: Request<RequestBody>,
    client_addr: SocketAddr, // For access control
    listen_addr: SocketAddr,
    tls_enabled: bool,
    tls_server_name: Option<ServerName>,
  ) -> RpxyResult<Response<ResponseBody>> {
    // Prepare log data only when the installed logger can actually emit an
    // access-log line; otherwise skip the field captures/clones entirely.
    let mut log_data = self
      .globals
      .access_log_enabled
      .then(|| HttpMessageLog::new(&req, self.globals.proxy_config.redact_query_in_access_log));
    if let Some(l) = log_data.as_mut() {
      l.client_addr(&client_addr);
    }

    // Capture before `req` is moved into the inner handler; needed by
    // `synthetic_error_response_with_close` on the PayloadTooLarge path (h1.x must close
    // the connection so the unread body bytes don't get fed into a recycled connection).
    let request_version = req.version();

    let http_result = self
      .handle_request_inner(&mut log_data, req, client_addr, listen_addr, tls_enabled, tls_server_name)
      .await;

    // passthrough or synthetic response
    match http_result {
      Ok(v) => {
        if let Some(l) = log_data.as_mut() {
          l.status_code(&v.status()).output();
        }
        Ok(v)
      }
      Err(e) => {
        // The request summary exists only while the access log is enabled;
        // quiet configurations keep the error and the client address.
        match log_data.as_mut() {
          Some(l) => error!("{e}: {l}"),
          None => error!("{e}: client={client_addr}"),
        }
        let close_connection = matches!(e, HttpError::PayloadTooLarge);
        let code = StatusCode::from(e);
        if let Some(l) = log_data.as_mut() {
          l.status_code(&code).output();
        }
        if close_connection {
          synthetic_error_response_with_close(code, request_version)
        } else {
          synthetic_error_response(code)
        }
      }
    }
  }

  /// Handle inner with no synthetic error response.
  /// Synthetic response is generated by caller.
  async fn handle_request_inner(
    &self,
    log_data: &mut Option<HttpMessageLog>,
    mut req: Request<RequestBody>,
    client_addr: SocketAddr, // For access control
    listen_addr: SocketAddr,
    tls_enabled: bool,
    tls_server_name: Option<ServerName>,
  ) -> HttpResult<Response<ResponseBody>> {
    // Here we start to inspect and parse with server_name. The parsed host is moved into the
    // owning `From<Vec<u8>>` conversion (in-place lowercase) rather than re-copied via a slice.
    let server_name = req
      .inspect_parse_host()
      .map(ServerName::from)
      .map_err(|_e| HttpError::InvalidHostInRequestHeader)?;

    // check consistency of between TLS SNI and HOST/Request URI Line.
    #[allow(clippy::collapsible_if)]
    if tls_enabled && self.globals.proxy_config.sni_consistency {
      if server_name != tls_server_name.unwrap_or_default() {
        return Err(HttpError::SniHostInconsistency);
      }
    }

    // Pre-flight Content-Length check: reject oversize requests with 413 before any
    // upstream contact. Placed AFTER host inspection and SNI consistency so that
    // malformed-host (400) / SNI-mismatch (421) requests get their specific status codes
    // even when they also carry an oversize Content-Length. The streaming-overrun case
    // is enforced separately at the body adapter (HTTP/1.x and HTTP/2 via the
    // `LimitedIncoming` wrapper, HTTP/3 via its dedicated body-forwarding task) and
    // surfaces as a body error rather than a clean 413.
    check_content_length_limit(&req, self.globals.proxy_config.request_max_body_size)?;
    // Find backend application for given server_name, and drop if incoming request is invalid as request.
    // `default_app` fallback is plaintext-HTTP only (see README and backend_main.rs). TLS requests
    // with an unknown host are rejected regardless of `sni_consistency`.
    let mut fallback_to_default_app = false;
    let backend_app = match self.app_manager.apps.get(&server_name) {
      Some(backend_app) => backend_app,
      None if !tls_enabled => {
        let default_server_name = self
          .app_manager
          .default_server_name
          .as_ref()
          .ok_or(HttpError::NoMatchingBackendApp)?;
        debug!("Serving by default app");
        fallback_to_default_app = true;
        self.app_manager.apps.get(default_server_name).unwrap()
      }
      None => return Err(HttpError::NoMatchingBackendApp),
    };

    // Redirect to https if !tls_enabled and redirect_to_https is true
    if !tls_enabled && backend_app.https_redirection.unwrap_or(false) {
      debug!("Redirect to secure connection: {}", backend_app.server_name);
      return secure_redirection_response(&backend_app.server_name, self.globals.proxy_config.public_https_port, &req);
    }

    // Find reverse proxy for given path and choose one of upstream host
    // Longest prefix match
    let path = req.uri().path();
    let upstream_candidates = backend_app.path_manager.get(path).ok_or(HttpError::NoUpstreamCandidates)?;

    // Upgrade in request header
    let upgrade_in_request = extract_upgrade(req.headers());
    if upgrade_in_request.is_some() && req.version() != http::Version::HTTP_11 {
      return Err(HttpError::FailedToUpgrade(format!(
        "Unsupported HTTP version: {:?}",
        req.version()
      )));
    }
    // let request_upgraded = req.extensions_mut().remove::<hyper::upgrade::OnUpgrade>();
    let req_on_upgrade = hyper::upgrade::on(&mut req);

    // If this request was matched via the `default_app` fallback, the incoming `Host` is untrusted.
    // Pass the matched backend's configured server_name through without allocating so that
    // `generate_request_forwarded` can force-overwrite `Host` after the usual header pass.
    let fallback_host = fallback_to_default_app.then_some(&backend_app.server_name);

    // Build request from destination information
    let _context = self
      .generate_request_forwarded(
        &client_addr,
        &listen_addr,
        &mut req,
        &upgrade_in_request,
        upstream_candidates,
        tls_enabled,
        fallback_host,
      )
      .map_err(|e| HttpError::FailedToGenerateUpstreamRequest(e.to_string()))?;

    debug!(
      "Request to be forwarded: [uri {}, method: {}, version {:?}, headers {:?}]",
      req.uri(),
      req.method(),
      req.version(),
      DebugHeaders::new(req.headers(), self.globals.unsafe_debug_headers)
    );
    if let Some(l) = log_data.as_mut() {
      l.xff(&req.headers().get(header_defs::X_FORWARDED_FOR));
      l.upstream(req.uri());
    }
    //////

    //////////////
    // Forward request to a chosen backend
    let mut res_backend = self
      .forwarder
      .request(req)
      .await
      .map_err(|e| HttpError::FailedToGetResponseFromBackend(e.to_string()))?;

    //////////////
    // Process reverse proxy context generated during the forwarding request generation.
    #[cfg(feature = "sticky-cookie")]
    if let Some(context_from_lb) = _context.context_lb {
      let sticky_config = _context
        .sticky_cookie_config
        .as_ref()
        .ok_or_else(|| HttpError::FailedToAddSetCookeInResponse("missing sticky-cookie config".to_string()))?;
      let cipher = self
        .globals
        .sticky_cookie_cipher
        .as_deref()
        .ok_or_else(|| HttpError::FailedToAddSetCookeInResponse("missing sticky-cookie cipher".to_string()))?;
      let res_headers = res_backend.headers_mut();
      if let Err(e) = set_sticky_cookie_lb_context(
        res_headers,
        &context_from_lb,
        sticky_config,
        _context.sticky_cookie_secure,
        cipher,
      ) {
        return Err(HttpError::FailedToAddSetCookeInResponse(e.to_string()));
      }
    }

    if res_backend.status() != StatusCode::SWITCHING_PROTOCOLS {
      // Generate response to client
      self
        .generate_response_forwarded(&mut res_backend, backend_app, tls_enabled)
        .map_err(|e| HttpError::FailedToGenerateDownstreamResponse(e.to_string()))?;
      return Ok(res_backend);
    }

    // Handle StatusCode::SWITCHING_PROTOCOLS in response
    let upgrade_in_response = extract_upgrade(res_backend.headers());
    let should_upgrade = match (upgrade_in_request.as_ref(), upgrade_in_response.as_ref()) {
      (Some(u_req), Some(u_res)) => u_req.eq_ignore_ascii_case(u_res),
      _ => false,
    };

    if !should_upgrade {
      return Err(HttpError::FailedToUpgrade(format!(
        "Backend tried to switch to protocol {:?} when {:?} was requested",
        upgrade_in_response, upgrade_in_request
      )));
    }
    // let Some(request_upgraded) = request_upgraded else {
    //   return Err(HttpError::NoUpgradeExtensionInRequest);
    // };

    // let Some(onupgrade) = res_backend.extensions_mut().remove::<hyper::upgrade::OnUpgrade>() else {
    //   return Err(HttpError::NoUpgradeExtensionInResponse);
    // };
    let res_on_upgrade = hyper::upgrade::on(&mut res_backend);

    self.globals.runtime_handle.spawn(async move {
      let mut response_upgraded = TokioIo::new(res_on_upgrade.await.map_err(|e| {
        error!("Failed to upgrade response: {}", e);
        RpxyError::FailedToUpgradeResponse(e.to_string())
      })?);
      let mut request_upgraded = TokioIo::new(req_on_upgrade.await.map_err(|e| {
        error!("Failed to upgrade request: {}", e);
        RpxyError::FailedToUpgradeRequest(e.to_string())
      })?);
      copy_bidirectional(&mut response_upgraded, &mut request_upgraded)
        .await
        .map_err(|e| {
          error!("Coping between upgraded connections failed: {}", e);
          RpxyError::FailedToCopyBidirectional(e.to_string())
        })?;
      Ok(()) as RpxyResult<()>
    });

    Ok(res_backend)
  }
}

/// Pre-flight `Content-Length` check.
///
/// Returns `Err(HttpError::PayloadTooLarge)` only when the inbound request advertises a
/// `Content-Length` that exceeds the configured `limit` (i.e. an oversize upload we can
/// detect *before* contacting the upstream). Returns `Ok(())` when the limit is `None`
/// (unlimited), when the header is absent or malformed (chunked / streamed bodies are
/// caught later by the body adapter), or when the announced length is within the limit.
fn check_content_length_limit<B>(req: &Request<B>, limit: Option<usize>) -> HttpResult<()> {
  let Some(limit) = limit else {
    return Ok(());
  };
  let Some(value) = req.headers().get(http::header::CONTENT_LENGTH) else {
    return Ok(());
  };
  // Malformed Content-Length: let the downstream parser (hyper) handle it; we are only
  // responsible for the size-bound check here. Parse as `u64` so that a syntactically
  // valid but very large value (e.g. > 4 GiB on a 32-bit target) does not silently fall
  // out of `s.parse::<usize>()` and skip the pre-flight 413; the comparison is widened
  // to `u64`.
  let Ok(s) = value.to_str() else { return Ok(()) };
  let Ok(n) = s.parse::<u64>() else { return Ok(()) };
  if n > limit as u64 {
    return Err(HttpError::PayloadTooLarge);
  }
  Ok(())
}

#[cfg(test)]
mod tests {
  use super::*;

  fn req_with_cl(value: &str) -> Request<()> {
    let mut req = Request::new(());
    req.headers_mut().insert(http::header::CONTENT_LENGTH, value.parse().unwrap());
    req
  }

  /// CL strictly above the limit -> 413.
  #[test]
  fn check_content_length_limit_rejects_oversize() {
    let req = req_with_cl("100");
    let err = check_content_length_limit(&req, Some(50)).unwrap_err();
    assert!(matches!(err, HttpError::PayloadTooLarge));
  }

  /// CL equal to the limit -> allow (boundary).
  #[test]
  fn check_content_length_limit_allows_equal_to_limit() {
    let req = req_with_cl("50");
    assert!(check_content_length_limit(&req, Some(50)).is_ok());
  }

  /// CL below the limit -> allow.
  #[test]
  fn check_content_length_limit_allows_under_limit() {
    let req = req_with_cl("10");
    assert!(check_content_length_limit(&req, Some(50)).is_ok());
  }

  /// Header absent (chunked / streamed) -> defer to the streaming body adapter; ok here.
  #[test]
  fn check_content_length_limit_passes_when_header_absent() {
    let req: Request<()> = Request::new(());
    assert!(check_content_length_limit(&req, Some(50)).is_ok());
  }

  /// Malformed CL value -> ok (let hyper handle the bad-request rejection downstream).
  #[test]
  fn check_content_length_limit_passes_on_malformed_header() {
    let req = req_with_cl("not-a-number");
    assert!(check_content_length_limit(&req, Some(50)).is_ok());
  }

  /// `None` limit -> unlimited; no rejection regardless of CL value.
  #[test]
  fn check_content_length_limit_unlimited_when_none() {
    let req = req_with_cl("999999999");
    assert!(check_content_length_limit(&req, None).is_ok());
  }

  /// `Some(0)` semantics: only the zero-length body passes (current h3 behaviour
  /// preserved for the unified limit).
  #[test]
  fn check_content_length_limit_zero_rejects_non_empty() {
    let req = req_with_cl("1");
    assert!(matches!(
      check_content_length_limit(&req, Some(0)).unwrap_err(),
      HttpError::PayloadTooLarge
    ));
    let req = req_with_cl("0");
    assert!(check_content_length_limit(&req, Some(0)).is_ok());
  }

  /// Regression: an oversize CL whose numeric value would overflow `usize` on a 32-bit
  /// target (and `u64::MAX` even on 64-bit) must still be rejected as PayloadTooLarge.
  /// Parsing as `u64` and comparing against `limit as u64` is what guarantees this; a
  /// previous `s.parse::<usize>()` would have failed on 32-bit and silently skipped the
  /// pre-flight 413.
  #[test]
  fn check_content_length_limit_rejects_value_overflowing_usize() {
    // Larger than usize::MAX on a 32-bit target (4_294_967_295) and larger than any
    // realistic `request_max_body_size`; still well within u64 (u64::MAX ~= 1.8e19).
    let req = req_with_cl("9999999999999");
    let err = check_content_length_limit(&req, Some(256 * 1024 * 1024)).unwrap_err();
    assert!(matches!(err, HttpError::PayloadTooLarge));
    // u64::MAX as a header value is still rejected (the largest value that parses).
    let req = req_with_cl(&u64::MAX.to_string());
    let err = check_content_length_limit(&req, Some(256 * 1024 * 1024)).unwrap_err();
    assert!(matches!(err, HttpError::PayloadTooLarge));
    // A value that does not even fit in `u64` is treated as malformed and deferred to
    // the streaming body adapter (consistent with the existing malformed-header test).
    let req = req_with_cl("99999999999999999999999999");
    assert!(check_content_length_limit(&req, Some(256 * 1024 * 1024)).is_ok());
  }
}
