/// Internal representation of the forwarding chain used by rpxy.
///
/// The flow is:
/// 1. Parse `Forwarded` and/or `X-Forwarded-*` into `ForwardedEntry`
/// 2. Append the immediate peer as rpxy's authoritative latest hop
/// 3. Trim trusted proxy hops from the right-hand side
/// 4. Regenerate outgoing `X-Forwarded-*` / `Forwarded` from the normalized chain
///
/// Keeping this representation in one place makes the trust-boundary logic
/// easier to audit than passing partially parsed headers across multiple helpers.

/// An entry in Forwarded header with only the parameters relevant for forwarding chain normalization and consistency check.
#[derive(Clone, Debug, PartialEq, Eq)]
struct ForwardedNode {
  ip: Option<IpAddr>,
  port: Option<String>,
  raw: ForwardedNodeRaw,
}

#[derive(Clone, Debug, PartialEq, Eq)]
enum ForwardedNodeRaw {
  Ip,
  Unknown,
  Obfuscated(String),
}

impl ForwardedNode {
  fn ip_addr(&self) -> Option<IpAddr> {
    self.ip
  }
}

#[derive(Clone, Debug, PartialEq, Eq)]
pub(super) struct ForwardedEntry {
  /// for=:
  /// For Forwarded Header: this is optional in the RFC, but we require it for each entry to be able to build the forwarding chain. If missing, the entry is considered invalid.
  for_node: ForwardedNode,
  /// proto=, When generated from incoming X-Forwarded-For, this is always None except for the last entry which was generated by the previous hop proxy. This is used for consistency check against X-Forwarded-Proto.
  proto: Option<String>,
  /// host=, When generated from incoming X-Forwarded-For, this is always None
  host: Option<String>,
  /// by=, When generated from incoming X-Forwarded-For, this is always None.
  by: Option<String>,
}

/// Add or update forwarding headers like `x-forwarded-for`.
/// If only `forwarded` header exists, it will update `x-forwarded-for` with the proxy chain.
/// If both `x-forwarded-for` and `forwarded` headers exist, it will update `x-forwarded-for` first and then add `forwarded` header.
/// If the immediate peer is in trusted_forwarded_proxies,
/// it will recursively trust the incoming forwarding headers and append the immediate peer IP to the end of the chain.
/// Otherwise, it will ignore incoming forwarding headers and start a new chain with the immediate peer IP.
pub(in crate::message_handler) fn add_forwarding_header(
  headers: &mut HeaderMap,
  client_addr: &SocketAddr,
  listen_addr: &SocketAddr,
  tls: bool,
  original_uri: &Uri,
  authoritative_host: Option<&str>,
  trusted_forwarded_proxies: &[IpNet],
) -> Result<()> {
  let peer_ip = canonicalize_ip(client_addr.to_canonical().ip());
  let has_forwarded = headers.contains_key(header::FORWARDED);
  let normalized_chain = normalize_forwarding_chain(headers, &peer_ip, tls, authoritative_host, trusted_forwarded_proxies);

  let (normalized_chain, xff_csv, xff_first_len) = match xff_csv_from_chain(&normalized_chain) {
    Some((csv, first_len)) => (normalized_chain, csv, first_len),
    None => {
      warn!("Normalized forwarding chain contains non-IP hops; falling back to peer-only forwarding view");
      // Only this rare fallback needs a freshly built peer entry, so build it here instead
      // of eagerly on every request.
      let peer_entry = build_peer_forwarded_entry(peer_ip, tls, authoritative_host);
      let mut csv = String::new();
      push_ip_addr(&mut csv, &peer_ip);
      let first_len = csv.len();
      (vec![peer_entry], csv, first_len)
    }
  };

  // For X-Real-IP: slice the first element out of the already-rendered CSV (one copy),
  // before X-Forwarded-For takes ownership of the whole buffer without copying. The value
  // equals formatting normalized_chain.first()'s IP directly, as before. Inserted further
  // below to keep the header insertion order unchanged.
  let x_real_ip_value = HeaderValue::from_str(&xff_csv[..xff_first_len])?;

  // Update X-Forwarded-For with normalized chain. insert() replaces all previous values,
  // identical to the remove+insert it replaces; try_from(String) takes ownership of the
  // CSV buffer without re-copying its bytes.
  headers.insert(X_FORWARDED_FOR, HeaderValue::try_from(xff_csv)?);

  // Preserve/update Forwarded only if it was present on input. If callers want to force
  // Forwarded generation, apply_upstream_options_to_header() will regenerate it later.
  if has_forwarded {
    match generate_forwarded_header(&normalized_chain) {
      Ok(forwarded_value) => add_header_entry_overwrite_if_exist(headers, header::FORWARDED, forwarded_value)?,
      Err(e) => warn!("Failed to update existing Forwarded header for consistency: {}", e),
    }
  } else {
    headers.remove(header::FORWARDED);
  }

  // Single line cookie header
  // TODO: This should be only for HTTP/1.1. For 2+, this can be multi-lined.
  make_cookie_single_line(headers)?;

  // Always overwrite these headers with rpxy's authoritative downstream view.
  // Constant values use HeaderValue::from_static (no runtime byte validation or allocation);
  // the port uses HeaderValue::from(u16) (lighter than to_string() + parse). insert() replaces
  // any existing values with a single value, matching add_header_entry_overwrite_if_exist.
  headers.insert(
    X_FORWARDED_PROTO,
    HeaderValue::from_static(if tls { "https" } else { "http" }),
  );
  headers.insert(X_FORWARDED_PORT, HeaderValue::from(listen_addr.port()));

  /////////// As Nginx-Proxy
  // x-real-ip: pre-built above from the first element of the XFF CSV.
  headers.insert(X_REAL_IP, x_real_ip_value);
  // x-forwarded-ssl
  headers.insert(X_FORWARDED_SSL, HeaderValue::from_static(if tls { "on" } else { "off" }));
  // x-original-uri
  add_header_entry_overwrite_if_exist(headers, X_ORIGINAL_URI, original_uri.to_string())?;
  // x-forwarded-host
  overwrite_x_forwarded_host_from_original(headers, authoritative_host)?;
  // proxy
  headers.insert(PROXY, HeaderValue::from_static(""));

  Ok(())
}

